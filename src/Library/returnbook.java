    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
         try
        {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/kasesesec","root","");
           String sql = "INSERT INTO returnbook VALUES (?,?,?,?,?)";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setString(1,txtnumber.getText());
           pst.setString(2,txtno.getText());
           String dater = ((JTextField)date.getDateEditor().getUiComponent()).getText();
           pst.setString(3,dater);
           pst.setString(4,txtauthority.getText());
           String course = txtcoment.getSelectedItem().toString();
           pst.setString(5, course);
           
           pst.executeUpdate();
           JOptionPane.showMessageDialog(this,"Textbook has been Returned and saved Successfully");
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);//"Please all the Fields must be filled");
        }

    }//GEN-LAST:event_btnsaveActionPerformed

    private void btncoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncoActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Kasesesec","root","");
           String sql = "select * from returnbook";
           PreparedStatement pst = con.prepareStatement(sql);
           //pst.setString(1,txtco.getText());
           ResultSet rs = pst.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            while(rs.next()){
                model.addRow(new String[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
                
            }
        }
        catch(Exception ex){
            System.out.println("Error : "+ex.getMessage());
            
            
        }
    }//GEN-LAST:event_btncoActionPerformed

