    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
         try
        {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Kasesesec","root","");
           String sql = "INSERT INTO newbooks (Bookid,Title,Author,Date,Authority) VALUES (?,?,?,?,?)";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setString(1,txtnumber.getText());
           pst.setString(2,txtname.getText());
           pst.setString(3,txtby.getText());
           String dater = ((JTextField)date.getDateEditor().getUiComponent()).getText();
        
           pst.setString(4,dater);
           pst.setString(5,txtissuer.getText());
           
           pst.executeUpdate();
           JOptionPane.showMessageDialog(this,"Textbook inserted Successfully");
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,"Textbook already exist in the database");
        }

    }//GEN-LAST:event_btnsaveActionPerformed

    private void btncoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncoActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Kasesesec","root","");
           String sql = "select * from newbooks";
           PreparedStatement pst = con.prepareStatement(sql);
           ResultSet rs = pst.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel)booktable.getModel();
            while(rs.next()){
                model.addRow(new String[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
                
            }
        }
        catch(Exception ex){
            System.out.println("Error : "+ex.getMessage());
            
            
        }
    }//GEN-LAST:event_btncoActionPerformed

