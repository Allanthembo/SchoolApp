    private void signinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinActionPerformed
        // TODO add your handling code here:
        String user = username.getText();
        String pass = password.getText();
        //case 1 if all password and username are blank
        if (user.equals("") && pass.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please Enter username and password");
        }
        else if (user.equals(""))//this one tests the username only
        {
            JOptionPane.showMessageDialog(this,"Please enter Username");
        }
        else if(pass.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Please enter Password");
        }
         else
        {
            try
        {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Kasesesec","root","");
           String sql = "SELECT * FROM admindetails where AdminNumber = ? and Surname =?";
           PreparedStatement pst = con.prepareStatement(sql);
           pst.setString(1,password.getText());
           pst.setString(2,username.getText());
           ResultSet rs = pst.executeQuery();
           
           if(rs.next())
           {
               JOptionPane.showConfirmDialog(null, "Username and Password match");
               work1.setText(rs.getString(3));
               work2.setText(rs.getString(2));
               work3.setText(rs.getString(4));
               Departments a = new Departments();
               a.show();
               this.setVisible(false);
               
           }
           else
           {
               JOptionPane.showConfirmDialog(null, "Incorrect Username and Password");
               username.setText("");
               password.setText("");
               
           }
           con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        }
    }//GEN-LAST:event_signinActionPerformed

